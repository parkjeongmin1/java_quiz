package ch13.quiz2;

public class Container<K, V> {
   private K key;
   private V value;

    public K getKey() {
        return key;
    }

    public void setKey(K 이름, String 직업) {
        this.key = key;
    }

    public V getValue() {
        return value;
    }

    public void setValue(String 이름, V 나이) {
        this.value = value;
    }
}
